
fun main() {
    for (i in 1..5) {
    	println(i)
	}
}

//while
fun main() {
    // Loop while
    var i = 1
    while (i <= 5) {
        println(i)
        i++
	}
}

//do-while
fun main() {
    // Loop do-while
    var i = 0
    do {
        println(i)
        i++
    } while (i <= 5) 
}

